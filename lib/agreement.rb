Avtale om taushetsplikt
Bedriftsnavn: _______________________ + IP/prosjekt/registreringsdata og The MadLab
Institute of Innovation (heretter kalt ”Partene”) har blitt enige om et samarbeid om
gjennomgang av dokumenter. I denne forbindelse har Partene inngått følgende avtale:
1. Definisjon
Med ”Konfidensiell Informasjon” skal i denne avtale forstås all informasjon som gis av en part
til den annen part som enten utgjør en forretningshemmelighet, er betegnet som en
forretningshemmelighet eller som av andre grunner er av en slik karakter at den ikke skal
være tilgjengelig for andre. Konfidensiell informasjon omfatter, men er ikke begrenset til
strategidokumenter, handlingsplaner, budsjetter, regnskap, tabellariske oppstillinger,
tekniske opplysninger, økonomimodeller, erfaringstall, prognoser, immaterielle rettigheter og
salg- og markedsføringsmetoder og -materiell.
2. Taushetsplikt
Hver av Partene vil motta informasjon om hverandre og den respektive parts virksomhet.
Partene forplikter seg til å besørge at alle deres ansatte og / eller konsulenter som vil få
tilgang til deler av eller all Konfidensiell Informasjon som omfattes av denne avtale, forut for
arbeidets påbegynnelse blir gjort oppmerksom på denne avtale og forpliktes til å holde all
Konfidensiell Informasjon hemmelig i samsvar med avtalen.
3. Forbud mot urettmessig bruk
Partene forplikter seg gjensidig til å avstå fra enhver bruk av Konfidensiell Informasjon som
tilhører en annen part med unntak av bruk i anledning samarbeidet mellom Partene.
4. Behandling og oppbevaring
Partene forplikter seg til å behandle, oppbevare og holde all Konfidensiell Informasjon
utilgjengelig for andre enn de som er parter i denne avtalen, inklusive ansatte hos Partene
som ikke utfører arbeid i tilknytning til samarbeidet mellom Partene.
Partene forplikter seg gjensidig til å returnere all skriftlig dokumentasjon som er mottatt fra en
annen part og som er Konfidensiell Informasjon, etter at samarbeidet er avsluttet, eller hvis
en part krever det.
5. Rettigheter
Partene er enige om at frigivelse Partene imellom av Konfidensiell Informasjon ikke
innebærer overføring av rettigheter, herunder immaterielle rettigheter, fra en part til en
annen.
6. Varighet
Taushetsplikten etter denne avtale gjelder uten tidsbegrensning, og forplikter dermed
Partene og deres ansatte også etter opphør av samarbeidet mellom Partene.
The MadLab Institute of Innovation Norway
7. Begrensning
Taushetsplikten oppheves dersom Konfidensiell Informasjon gjøres offentlig kjent av den part
som har krav på hemmelighold, eller offentliggjøres med samtykke fra denne parten.
Denne avtalen er utferdiget i to eksemplarer, hvorav partene beholder hvert sitt.
